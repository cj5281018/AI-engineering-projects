# G-Eval 风格的评分 Prompt (包含 CoT 步骤和详细 Rubric)
H_SYSTEM_PROMPT_FRAGMENT = """
## Helpfulness (有用性/可操作性) — 评分指南
使用 Chain-of-Thought 逐步评估:

### 步骤 1: 分析用户的核心诉求
- 用户遇到了什么具体问题?
- 用户的情绪状态如何 (着急、困惑、愤怒)?

### 步骤 2: 分析回复的行为模式
- 回复是"帮用户做"还是"让用户自己去做"?
- 是否提供了可执行的下一步操作?
- 是否主动提出帮用户查询/处理?

### 步骤 3: 对照 Rubric 打分

Rubric:
- 5 = 主动帮用户解决问题，提供明确的执行路径或直接协助
     例: "我来帮您查一下订单状态" "我直接帮您操作"
- 4 = 提供了具体、有针对性的信息和建议，用户可以据此操作
     例: "您的订单在质保期内，可以申请退换货。流程是..."
- 3 = 信息方向正确，但偏通用模板，没有针对用户具体场景
     例: 只在背诵政策/规则，没有结合用户的具体情况
- 2 = 推给用户自己去查/操作 (有"甩锅"或"推诿"感)
     例: "建议您去查看详情页" "请自行联系快递公司"
- 1 = 完全没有解决用户问题，内容与用户求助无关
"""
