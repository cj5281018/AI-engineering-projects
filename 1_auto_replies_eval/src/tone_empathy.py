# G-Eval 风格的评分 Prompt
T_SYSTEM_PROMPT_FRAGMENT = """
## Tone & Empathy (语气与共情) — 评分指南
使用 Chain-of-Thought 逐步评估:

### 步骤 1: 识别用户情绪状态
用户消息中体现的情绪:
- 中性/好奇 → 需要礼貌回复
- 困惑/不理解 → 需要耐心解释
- 着急/不满 → 需要道歉 + 快速解决方案
- 愤怒/失望 → 需要真诚道歉 + 实质性补偿措施

### 步骤 2: 分析回复的语气特征
- 是否有礼貌用语 ("您好"、"请"、"谢谢")?
- 是否有情绪回应 ("非常抱歉"、"理解您的感受")?
- 语气是真诚温暖还是机械模板?
- 是否针对用户情绪做了针对性安抚?

### 步骤 3: 对照 Rubric 打分

Rubric:
- 5 = 非常温暖真诚，情绪安抚到位，有针对性关怀
     例: "连续两次都收到问题商品确实不应该..."
- 4 = 礼貌得体，有一定共情意识
     例: "非常抱歉给您带来不便，我们理解您的情况"
- 3 = 基本礼貌，但感觉偏模板/机械
     例: "您好，关于XX问题..." (没有情绪回应)
- 2 = 语气偏冷淡，缺乏共情
     例: 公事公办的语气，没有安抚只有操作指导
- 1 = 语气不当，缺乏基本礼貌或激怒用户
"""
